const getFractionInfo = (cards: Card[]) => {
  const amount = cards.length;
  let totalValues = 0;
  let totalCost = 0;
  let rareCards = 0;
  let commonCards = 0;

  cards.forEach((card) => {
    totalValues += card.value;
    totalCost += card.cost;
    if (card.rarity === 'Common') {
      commonCards += 1;
    } else {
      rareCards += 1;
    }
  });

  return {
    amount,
    totalCost,
    totalValues,
    rareCards,
    commonCards,
    average: +(totalValues / totalCost).toFixed(5),
  };
};

